std::vector<glm::vec3> resizeObject(float multiplier, std::vector<glm::vec3> vertices, int vbd_size)
{
	for (int i = 0; i < vbd_size; i++)
	{
		vertices[i].x *= multiplier;
		vertices[i].r *= multiplier;
		vertices[i].s *= multiplier;

		vertices[i].y *= multiplier;
		vertices[i].g *= multiplier;
		vertices[i].t *= multiplier;

		vertices[i].z *= multiplier;
		vertices[i].b *= multiplier;
		vertices[i].p *= multiplier;
	}
	return vertices;
}

void resizeObject(float multiplier, GLfloat* g_vertex_buffer_data, int vbd_size)
{
	for (int i = 0; i < vbd_size; i++)
	{
		g_vertex_buffer_data[i] *= multiplier;
	}
	
}

void calculatePlaneBufferData(GLfloat* g_vertex_buffer_data) // , int sizeX, int sizeY, float zOffset
{
	for (int x = 0; x < 19; ++x)
	{
		for (int y = 0; y < 19; ++y)
		{
			// 0,0 0,1 0,2 1,0 1,1 1,2
			int offset = (x*19 + y)*18;

			// first triangle of square
			g_vertex_buffer_data[offset + 0]  = 5*(float) x; //x
			g_vertex_buffer_data[offset + 1]  = 5*(float) y; //y
			g_vertex_buffer_data[offset + 2]  = 0.0f; //z

			g_vertex_buffer_data[offset + 3]  = 5*((float) x + 1);
			g_vertex_buffer_data[offset + 4]  = 5*(float) y;
			g_vertex_buffer_data[offset + 5]  = 0.0f;

			g_vertex_buffer_data[offset + 6]  = 5*((float) x + 1);
			g_vertex_buffer_data[offset + 7]  = 5*((float) y + 1);
			g_vertex_buffer_data[offset + 8]  = 0.0f;

			// second triangle of square
			g_vertex_buffer_data[offset + 9]  = 5*(float) x;
			g_vertex_buffer_data[offset + 10] = 5*(float) y;
			g_vertex_buffer_data[offset + 11] = .0f;

			g_vertex_buffer_data[offset + 12] = 5*(float) x;
			g_vertex_buffer_data[offset + 13] = 5*((float) y + 1);
			g_vertex_buffer_data[offset + 14] = 0.0f;

			g_vertex_buffer_data[offset + 15] = 5*((float) x + 1);
			g_vertex_buffer_data[offset + 16] = 5*((float) y + 1);
			g_vertex_buffer_data[offset + 17] = 0.0f;
		}
	}
}

